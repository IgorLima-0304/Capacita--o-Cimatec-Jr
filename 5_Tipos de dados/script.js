//JavaScript é uma linguagem de tipagem dinâmica, o que significa que o tipo de dado de uma variável é determinado automaticamente em tempo de execução. 
//Os principais tipos de dados incluem:
//--> 1. Primitivos: <--
//number: Números inteiros ou de ponto flutuante.
//string: Sequências de caracteres.
//boolean: Valores true ou false.
//null: Valor nulo ou ausente.
//undefined: Valor indefinido.
//symbol: Um tipo primitivo para criar identificadores únicos.

//--> 2. Objetos: <--
//object: Coleção de pares chave-valor.

//--> Atribuição de Valores: <--
//Você pode atribuir valores a variáveis usando o operador de atribuição (=):
let x = 10;
let nome = "Igor";

//--> Operações e Manipulação: <--
//Você pode realizar várias operações e manipulações com variáveis, dependendo de seus tipos. Alguns exemplos:
let a = 5;
let b = 10;

let soma = a + b;
let concat = nome + "Lima";
